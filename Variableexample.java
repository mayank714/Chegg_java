class Variableexample{
    int y1=25;// this is instance variable
    static int y2 = 234;// this is static variable 

    public static void main(String args[]){
        //these all are local variable inside the method
        int x = 25;
        byte x1 = 24;
        char x2 = 'M';
        long x3 = 155;
        short x4 = 12445;
        float x5 = 1.223f;//in float we have to write "f" in the last
        double x6 = 50.512;

        System.out.println(x);
        System.out.println(x1);
        System.out.println(x2); 
        System.out.println(x3);
        System.out.println(x4);
        System.out.println(x5);
        System.out.println(x6);
    }
}